package com.model;

import javax.persistence.*;

/**
 *
 * @author mounika
 */

@Entity
@Table(name="system_type")
public class SystemType {
    @Id
    @GeneratedValue(strategy =GenerationType.IDENTITY)
    private int id;
    
    @Column(name="name")
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
        
}
